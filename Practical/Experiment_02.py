# Write a program to perform different Arithmetic Operations on numbers in Python,

num01 = int(input("Enter 1st number : "))
num02 = int(input("Enter 2nd number : "))

sum = num01 + num02
diff = num01 - num02
product = num01 * num02
division = num01 / num02
rem = num01 % num02
floor_div = num01 // num02
power = num01 ** num02

print("Sum of two number :",sum)
print("Difference of two number :",diff)
print("Multiplication of two number :",product)
print("Division of two number :",division)
print("Remainder of two number :",rem)
print("Floor_div of two number :",floor_div)
print("Power of two number :",power)